/*EXEMPLO ESCOLA DE CURSOS*/

CREATE TABLE CURSOS(
	ID_CURSO INT PRIMARY KEY AUTO_INCREMENT,
	NOME VARCHAR(30) NOT NULL,
	HORAS INT(3) NOT NULL,
	VALOR FLOAT(10, 2) NOT NULL
);

INSERT INTO CURSOS VALUES(NULL, 'JAVA', 30, 500.00);
INSERT INTO CURSOS VALUES(NULL, 'BANCO DE DADOS', 40, 700.00);


DELIMITER $

CREATE PROCEDURE CAD_CURSOS(P_NOME VARCHAR(30),
							P_HORAS INT(3),
							P_VALOR FLOAT(10,2))

BEGIN
	INSERT INTO CURSOS VALUES (NULL, P_NOME, P_HORAS, P_VALOR);
END
$

DELIMITER ;

CALL CAD_CURSOS('SQL', 35, 3000.00);
CALL CAD_CURSOS('PHP', 35, 3000.00);
CALL CAD_CURSOS('JAVA', 35, 3000.00);

SELECT * FROM CURSOS;


/*PROCEDURE PARA CONSULTAR CURSOS*/
DELIMITER $

CREATE PROCEDURE CONS_CURSOS()
BEGIN
	SELECT NOME, HORAS, VALOR FROM CURSOS;
END
$

DELIMITER ;

CALL CONS_CURSOS();

